public class EjerciciosTema1 {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {


        // Tipos de datos
        byte numeroByte = 10;
        short numeroShort = 1000;
        int numeroInt = 100000;
        float numeroFloat = 3.1416f;
        double numeroDouble = 3.14159265359;
        boolean esVerdadero = true;
        char caracter = 'A';
        String texto = "Hola, mundo!";

        // Imprimir los datos
        System.out.println("Número byte: " + numeroByte);
        System.out.println("Número short: " + numeroShort);
        System.out.println("Número int: " + numeroInt);
        System.out.println("Número float: " + numeroFloat);
        System.out.println("Número double: " + numeroDouble);
        System.out.println("¿Es verdadero?: " + esVerdadero);
        System.out.println("Carácter: " + caracter);
        System.out.println("Texto: " + texto);
    }
}

    
    

